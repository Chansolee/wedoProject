package kr.or.ddit.wedo.vo;

public class OneToOneQnaVO {
	private String one_qna_title;
	private String one_qna_content;
	private String one_qna_date;
	private int one_qna_no;
	private String mem_id;
	private String teacher_id;
	private String mem_name;
	private String teacher_name;
	
	public String getMem_name() {
		return mem_name;
	}
	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}
	public String getTeacher_name() {
		return teacher_name;
	}
	public void setTeacher_name(String teacher_name) {
		this.teacher_name = teacher_name;
	}
	public String getOne_qna_title() {
		return one_qna_title;
	}
	public void setOne_qna_title(String one_qna_title) {
		this.one_qna_title = one_qna_title;
	}
	public String getOne_qna_content() {
		return one_qna_content;
	}
	public void setOne_qna_content(String one_qna_content) {
		this.one_qna_content = one_qna_content;
	}
	public String getOne_qna_date() {
		return one_qna_date;
	}
	public void setOne_qna_date(String one_qna_date) {
		this.one_qna_date = one_qna_date;
	}
	public int getOne_qna_no() {
		return one_qna_no;
	}
	public void setOne_qna_no(int one_qna_no) {
		this.one_qna_no = one_qna_no;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getTeacher_id() {
		return teacher_id;
	}
	public void setTeacher_id(String teacher_id) {
		this.teacher_id = teacher_id;
	}
	
	
}
